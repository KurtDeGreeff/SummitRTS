function Pause($Sleep=10) {
	start-sleep -s $Sleep
}

#=======================================================================================
function GetTimestamp{
	return $(get-date).ToString("yyyy-MM-dd HH:mm:ss")
}

#=======================================================================================
function UnixTime($tDate=$Null) {
	
	if (! $tDate) {$tDate = Get-Date}
	return [double] (Get-Date($tDate) -UFormat %s)
}

